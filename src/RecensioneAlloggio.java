
public class RecensioneAlloggio extends Recensione {
	private int recensioneID;
	private int valutazionePulizia;
	private int valutazioneServizio;
	private int valutazionePosizione;
	
	public int getRecensioneID() {
		return recensioneID;
	}
	public void setRecensioneID(int recensioneID) {
		this.recensioneID = recensioneID;
	}
	public int getValutazionePulizia() {
		return valutazionePulizia;
	}
	public void setValutazionePulizia(int valutazionePulizia) {
		this.valutazionePulizia = valutazionePulizia;
	}
	public int getValutazioneServizio() {
		return valutazioneServizio;
	}
	public void setValutazioneServizio(int valutazioneServizio) {
		this.valutazioneServizio = valutazioneServizio;
	}
	public int getValutazionePosizione() {
		return valutazionePosizione;
	}
	public void setValutazionePosizione(int valutazionePosizione) {
		this.valutazionePosizione = valutazionePosizione;
	}
}
